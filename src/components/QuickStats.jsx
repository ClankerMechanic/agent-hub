function formatRelativeTime(timestamp) {
  if (!timestamp) return 'Never';

  const now = Date.now();
  const diff = now - timestamp;
  const minutes = Math.floor(diff / 60000);
  const hours = Math.floor(diff / 3600000);
  const days = Math.floor(diff / 86400000);

  if (minutes < 1) return 'Just now';
  if (minutes < 60) return `${minutes}m ago`;
  if (hours < 24) return `${hours}h ago`;
  if (days < 7) return `${days}d ago`;

  return new Date(timestamp).toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric'
  });
}

export function QuickStats({ runCount, lastUsed }) {
  const formattedLastUsed = formatRelativeTime(lastUsed);

  return (
    <div className="grid grid-cols-2 gap-4">
      <div className="bg-white rounded-xl shadow-sm border border-gray-100 p-4">
        <div className="text-2xl font-bold text-gray-900">{runCount}</div>
        <div className="text-sm text-gray-500">Total runs</div>
      </div>
      <div className="bg-white rounded-xl shadow-sm border border-gray-100 p-4">
        <div className="text-2xl font-bold text-gray-900">{formattedLastUsed}</div>
        <div className="text-sm text-gray-500">Last used</div>
      </div>
    </div>
  );
}
